        String hidenSize = hiddelLayersSize.getText();
        String trainingMehode = trainingSize.split(",")[1];
        double learning=Double.parseDouble(lerningRete);
        int nombreItera=Integer.parseInt(nombreIterations);
        System.out.println("learning rate : "+learning+"  nombreIteration : "+nombreItera);
        int[][] mat;
        int trainingSIze = Integer.parseInt(trainingSize.split(",")[0]);
        String[] hiddenSplited = hidenSize.split(",");
        networkArchitecture = new int[hiddenSplited.length + 2];
        int inputSize = Utility.getAttributes(dataSetFile.getAbsolutePath()).size() - 1;
        networkArchitecture[0] = inputSize;
        networkArchitecture[networkArchitecture.length - 1] = 1;
        int k = 0;
        for (int i = 1; i < networkArchitecture.length - 1; i++) {
            networkArchitecture[i] = Integer.parseInt(hiddenSplited[k]);
            k++;
        }
        network = new NeuralNetwork(networkArchitecture);
        if (trainingMehode.equals("croise")) {
            // look for the dataset Type
            double app, numCorr = 0;
            if (dataSetFile.getName().contains("bank")) {
                ArrayList<BankInstance> bankInstances = FichierVersListeInstance.file2BankInstance(dataSetFile.getAbsolutePath());
                bankEncoder = FichierVersListeInstance.bankMapping;
                ageInterval = Utility.getIntervalls(bankInstances, 4, "age");
                incomeInterval = Utility.getIntervalls(bankInstances, 10, "income");
                bankInstances.forEach(bank -> bank.encode(FichierVersListeInstance.bankMapping, ageInterval, incomeInterval));
                network = Functions.crossTraining(network, nombreItera, learning, bankInstances, null, trainingSIze);
                weatherBtn.setEnabled(false);
                bankBtn.setEnabled(true);
                ArrayList<Double> prediction = new ArrayList<>();
                for (BankInstance bk : bankInstances) {
                    double[] predicted = network.feedForward(bk.getEncodedAttributes());
                    prediction.add(predicted[0]);
                }
                System.out.println("matrice confusion");
                mat = Functions.matriceConfusion(prediction);

            } else {
                ArrayList<Instance> instances = FichierVersListeInstance.file2Instance(dataSetFile.getAbsolutePath());
                instances.forEach(inst -> inst.encode(FichierVersListeInstance.attributesMapping));
                instanceEncoder = FichierVersListeInstance.attributesMapping;
                network = Functions.crossTraining(network, nombreItera, learning, null, instances, trainingSIze);
                weatherBtn.setEnabled(true);
                bankBtn.setEnabled(false);
                System.out.println("first:  " + instances.get(0).toString());
                // prediction et confusion
                ArrayList<Double> prediction = new ArrayList<>();
                System.out.println("instances size: " + instances.size());
                for (Instance in : instances) {

                    double[] predicted = network.feedForward(in.getEncodedFeatures());
                    prediction.add(predicted[0]);
                }
                System.out.println("matrice confusion");
                mat = Functions.matriceConfusion(prediction);

            }
        } else {
            if (dataSetFile.getName().contains("bank")) {
                ArrayList<BankInstance> bankInstances = FichierVersListeInstance.file2BankInstance(dataSetFile.getAbsolutePath());
                ageInterval = Utility.getIntervalls(bankInstances, 4, "age");
                incomeInterval = Utility.getIntervalls(bankInstances, 10, "income");
                bankInstances.forEach(bank -> bank.encode(FichierVersListeInstance.bankMapping, ageInterval, incomeInterval));
                bankEncoder = FichierVersListeInstance.bankMapping;
                network = Functions.splitTraining(network, nombreItera, learning, bankInstances, null, trainingSIze);
                weatherBtn.setEnabled(false);
                bankBtn.setEnabled(true);
                ArrayList<Double> prediction = new ArrayList<>();
                ArrayList<BankInstance> testingData = Functions.testBanking;
                for (BankInstance in : testingData) {

                    double[] predicted = network.feedForward(in.getEncodedAttributes());
                    prediction.add(predicted[0]);
                }
                System.out.println("matrice confusion");
                mat = Functions.matriceConfusion(prediction);

            } else {
                ArrayList<Instance> instances = FichierVersListeInstance.file2Instance(dataSetFile.getAbsolutePath());
                instances.forEach(bank -> bank.encode(FichierVersListeInstance.attributesMapping));
                instanceEncoder = FichierVersListeInstance.attributesMapping;
                network = Functions.splitTraining(network, nombreItera, learning, null, instances, trainingSIze);
                weatherBtn.setEnabled(true);
                bankBtn.setEnabled(false);
                ArrayList<Double> prediction = new ArrayList<>();
                ArrayList<Instance> testingData = Functions.testInstances;
                for (Instance in : testingData) {

                    double[] predicted = network.feedForward(in.getEncodedFeatures());
                    prediction.add(predicted[0]);
                }
                System.out.println("matrice confusion");
                mat = Functions.matriceConfusion(prediction);

            }
        }
        for (int i = 0; i < mat.length; i++) {
            for (int j = 0; j < mat.length; j++) {
                    System.out.print(mat[i][j]+" ");
            }
            System.out.println("");
        }
        mat00.setText(mat[0][0] + "");
        mat01.setText(mat[0][1] + "");
        mat10.setText(mat[1][0] + "");
        mat11.setText(mat[1][1] + "");
        precisionYes = Functions.precisionClass(mat, 0) * 100;
        precisionNo = Functions.precisionClass(mat, 1) * 100;
        rappelYes = Functions.rappelClass(mat, 0) * 100;
        rappelNo = Functions.rappelClass(mat, 1) * 100;
        fmeseurYes = Functions.fmeseurClass(precisionYes, rappelYes) ;
        fmeseurNo = Functions.fmeseurClass(precisionNo, rappelNo);
        RYes.setText(String.format("%.2f", rappelYes) + " %");
        PYes.setText(String.format("%.2f", precisionYes) + " %");
        FYes.setText(String.format("%.2f", fmeseurYes) + " %");
